
def format_tiet(data: str):
    """
        Tiết thành thời gian bắt đầu và kết thúc
    """
    datas_mapping = {
        '1,2,3': '07:00:00 - 09:25:00',
        '4,5,6': '09:35:00 - 12:00:00',
        '7,8,9': '13:00:00 - 15:25:00',
        '10,11,12': '15:35:00 - 18:00:00',
        '1': '07:00:00 - 07:45:00',
        '2': '07:50:00 - 08:35:00',
        '3': '08:40:00 - 09:25:00',
        '4': '09:35:00 - 10:20:00',
        '5': '10:25:00 - 11:10:00',
        '6': '11:15:00 - 12:00:00',
        '7': '13:00:00 - 13:45:00',
        '8': '13:50:00 - 14:35:00',
        '9': '14:40:00 - 15:25:00',
        '10': '15:35:00 - 16:20:00',
        '11': '16:25:00 - 17:10:00',
        '12': '17:15:00 - 18:00:00',
        '13': '18:15:00 - 19:00:00',
        '14': '19:05:00 - 19:50:00',
        '15': '19:55:00 - 20:40:00',
        '16': '20:45:00 - 21:30:00',
    }
    
    tgbd, tgkt = datas_mapping[data].split(' - ')
    
    return [tgbd, tgkt]